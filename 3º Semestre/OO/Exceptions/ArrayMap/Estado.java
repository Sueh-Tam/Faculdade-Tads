public class Estado {
    
    private String nome;
    private String regiao;
    
    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    
    public void setRegiao(String regiao) {
        this.regiao = regiao;
    }
    public String getRegiao() {
        return regiao;
    }

}
