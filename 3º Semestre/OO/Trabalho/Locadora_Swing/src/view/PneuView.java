    private void jtPneusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtPneusMouseClicked
        tfID.setText(
                jtPneus.getValueAt(jtPneus.getSelectedRow(), 
                        VeiculoTableModel.COL_CODIGO).toString()
        );
        tfSerie.setText(
                jtPneus.getValueAt(jtPneus.getSelectedRow(), 
                        VeiculoTableModel.COL_MARCA).toString()
        );
        tfCor.setText(
                jtPneus.getValueAt(jtPneus.getSelectedRow(), 
                        VeiculoTableModel.COL_MODELO).toString()
        );
        tfCod.setText(
                jtPneus.getValueAt(jtPneus.getSelectedRow(), 
                        VeiculoTableModel.COL_CHASSI).toString()
        );
        tfEstado.setText(
                jtPneus.getValueAt(jtPneus.getSelectedRow(), 
                        VeiculoTableModel.COL_ANO).toString()
        );
        btExcluir.setEnabled(true);
    }//GEN-LAST:event_jtPneusMouseClicked

