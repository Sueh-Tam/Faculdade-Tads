public class Carro {
    private int id;
    private String placa;
    private String marca;
    private String modelo;
    private int codPneuAlugado;

    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }

    public String getPlaca() {
        return placa;
    }
    public void setPlaca(String placa) {
        this.placa = placa;
    }

    public String getMarca() {
        return marca;
    }
    public void setMarca(String marca) {
        this.marca = marca;
    }

    public String getModelo() {
        return modelo;
    }
    public void setModelo(String modelo) {
        this.modelo = modelo;
    }

    public int getCodPneuAlugado() {
        return codPneuAlugado;
    }
    public void setCodPneuAlugado(int codPneuAlugado) {
        this.codPneuAlugado = codPneuAlugado;
    }

    
    
}
